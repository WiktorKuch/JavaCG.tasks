package pl.codegym.task.task03.task0322;


/* 
Głęboka i czysta miłość
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        InputStream inputStream = System.in;
         Reader inputStreamReader = new InputStreamReader(inputStream);
         BufferedReader bufferedReader = new BufferedReader(inputStreamReader);
         
         String imie1 = bufferedReader.readLine();
         String imie2 = bufferedReader.readLine();
         String imie3 = bufferedReader.readLine();
         
         System.out.println(imie1+" "+"+"+" "+imie2 +" "+"+"+" "+imie3+" "+"=");
         System.out.println("Czysta miłość. Ooo la la!");
        
        
        
        
        
        
        
    }
}